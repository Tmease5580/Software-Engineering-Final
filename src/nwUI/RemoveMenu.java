    private void yesBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_yesBActionPerformed
        //remove element from model at same index in arraylist as jlist
        //currently to check if element was removed, I'm making sure the size decreased
        //but there's a way to check if a specific object was removed if you prefer to do it that way
        //m.contains(Object)
        int initialSize = m.getSize();
        Object itemName = m.get(s.getSelectedIndex());
        m.removeElementAt(s.getSelectedIndex());
        Cart cart = Cart.getInstance();
        if(s.getModel()==CartMenu.groceryModel){
            String toBeRemoved = (String)itemName;
            ArrayList<Item> items = cart.getItems();
            boolean wasRemoved = false;
            for(int i =0; i<items.size()&&!wasRemoved;i++){
                Item check = items.get(i);
                String nameCheck = check.getName()+"      $"+check.getPrice();
                if(nameCheck.equalsIgnoreCase(toBeRemoved)){
                    items.remove(check);
                    wasRemoved = true;
                    Cart.getInstance().setItems(items);
                }
            }
        }else{
            String toBeRemoved = (String)itemName;
            ArrayList<Sandwich> orders = cart.getOrders();
            boolean wasRemoved = false;
            for(int i =0; i<orders.size()&&!wasRemoved;i++){
                Sandwich check = orders.get(i);
                String costString = String.format("%.2f",check.cost());
                String nameCheck = orders.get(i).getName()+"   $"+costString;
                if(nameCheck.equalsIgnoreCase(toBeRemoved)){
                    orders.remove(check);
                    wasRemoved = true;
                    Cart.getInstance().setOrders(orders);
                }
            }
        }
        if(initialSize>m.getSize()){
            l.setText("Removed "+itemName+" from cart.");
        }else{
            l.setText("Unexpected error. Please try again.");
        }
        updateSubtotal();
        String newSubtotal = String.format("$%.2f",CartMenu._subtotal);
        subtotal.setText(newSubtotal);
        dispose();
    }//GEN-LAST:event_yesBActionPerformed

