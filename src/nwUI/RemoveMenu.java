    private void updateSubtotal(){
        double newSubtotal = 0;
        Cart cart = Cart.getInstance();
        ArrayList<Item> groceries = cart.getItems();
        ArrayList<Sandwich> orders = cart.getOrders();
        for(int i = 0;i<groceries.size();i++){
            Item toAdd = groceries.get(i);
            newSubtotal=newSubtotal+toAdd.getPrice();
        }
        for(int i = 0;i<orders.size();i++){
            Sandwich toAdd = orders.get(i);
            newSubtotal=newSubtotal+toAdd.cost();
        }
        CartMenu._subtotal = newSubtotal;
    }
